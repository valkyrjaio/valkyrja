namespace Valkyrja\Tests\Unit\Event\Generator;

use Valkyrja\Application\Directory\Directory;
use Valkyrja\Event\Generator\DataProviderFileGenerator;
use Valkyrja\Support\Generator\Enum\GenerateStatus;
use Valkyrja\Tests\Unit\Abstract\TestCase;

final class DataProviderFileGeneratorTest extends TestCase
{
    public function testGenerateFile(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'EventDataTestDataProvider';
        $filePath   = "$directory/$className.php";
        $generator  = new DataProviderFileGenerator(
            directory: $directory,
            namespace: 'App',
            className: $className,
            dataClassNamespace: 'App\\Provider\\Data\\EventData',
            dataClassName: 'EventData'
        );
        $results   = $generator->generateFile();

        self::assertSame(GenerateStatus::SUCCESS, $results);
        self::assertSame($generator->generateFileContents(), @file_get_contents($filePath));

        @unlink($filePath);
    }

    public function testGenerateFileContents(): void
    {
        $directory  = Directory::storagePath();
        $className  = 'EventDataTestDataProvider';
        $namespace  = 'App';
        $generator  = new DataProviderFileGenerator(
            directory: $directory,
            namespace: $namespace,
            className: $className,
            dataClassNamespace: 'App\\Provider\\Data\\EventData',
            dataClassName: 'EventData'
        );
        $contents  = $generator->generateFileContents();

        $expected = <<<PHP
            <?php

            declare(strict_types=1);

            // This file was automatically generated by the Valkyrja Framework.

            namespace $namespace;

            use App\Provider\Data\EventData;use Valkyrja\Container\Manager\Contract\ContainerContract;use Valkyrja\Container\Provider\Abstract\Provider;use Valkyrja\Event\Data\Data;

            final class EventDataTestDataProvider extends Provider
            {
                /**
                 * @inheritDoc
                 */
                #[Override]
                public static function publishers(): array
                {
                    return [
                        Data::class => [self::class, 'publishData'],
                    ];
                }

                /**
                 * @inheritDoc
                 */
                #[Override]
                public static function provides(): array
                {
                    return [
                        Data::class,
                    ];
                }

                /**
                 * Publish the service.
                 */
                public static function publishData(ContainerContract \$container): void
                {
                    \$container->setSingleton(Data::class, new EventData());
                }
            }

            PHP;

        self::assertNotEmpty($contents);
        self::assertSame($expected, $contents);
    }
}
